public class Autos {
    private String color;
    private int number;
    private int power;

    void setColor(String color){
        this.color = color;
    }
    void setNumber(int number){
        this.number = number;
    }
    void setPower(int power){
        this.power = power;
    }

    String getColor() {
        return color;
    }
    int getNumber() {
        return number;
    }
    int getPower() {
        return power;
    }

}
